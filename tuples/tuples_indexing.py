names = ("Joe", "Jane", "Jack", "Jill", "Mark", "Mary", "Mike", "Molly")

# read values
print(names[0])
print(names[3])
# print(names[4]) # error, index out of range

print(names[0:2])

# last item
print(names[-1])

# last 3 items
print(names[-3:])

# range of index
print(names[0:4])
print(names[0:3])
print(names[0:4:2]) # start, stop, step

# slicing
print(names[2:4])

print(names[-3:-1])
print(names[::-1]) # reverse


