# from math impor sin, cos
import math

#round
print(math.ceil(2.5)) # 3
print(math.floor(2.5)) # 2
print(math.trunc(2.5)) # 2

# sin cos
print(math.sin(0.5))
print(math.cos(0.5))
print(math.tan(0.5))
print(math.tan(0.5))
print(math.exp(2)) # e^2, exponential
print(math.cos(math.radians(90))) # 0.0


# log
print(math.log(2)) # 1.0

print(math.sqrt(4)) # 2.0
print(math.pow(2, 3)) # 8.0
print(math.pi)
print(math.e)

# fabs, absolute value of a float
print(math.fabs(-2.5)) # 2.5
print(math.fabs(2.5)) # 2.5
print(math.fabs(0)) # 0.0

# factorial
print(math.factorial(5)) # 120
print(math.factorial(0)) # 1
