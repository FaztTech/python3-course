names = ['John', 'Paul', 'George', 'Ringo', 'Pete']

print(names[0:3]) # print first 3 names
print(names[2:5]) # print last 3 names
print(names[1:4]) # print middle 3 names
print(names[0:5]) # print all names
print(names[0:5:2]) # print every other name
print(names[:]) # print all names

print(names[:3]) # print first 3 names
print(names[2:]) # print last 3 names

print(names[-3:]) # print last 3 names
print(names[-5:]) # print all names
print(names[-5::2]) # print every other name
print(names[::2]) # print every other name
print(names[::-1]) # print names in reverse
print(names[::-2]) # print every other name in reverse
print(names[4:0:-1]) # print names in reverse
print(names[4::-1]) # print names in reverse


