# Numbers
print(30) # integers
print(30.1) # floats
print(30j) # complex

# type
print(type(30)) # <class 'int'>
print(type(30.1)) # <class 'float'>
print(type(30j)) # <class 'complex'>

print(type(5 + 3.14)) # <class 'float'>

# Integers
print(2 + 3) #5 print an integer
# 2 + "3" # this returns an error
# "2" + "3" # returns concatenation
print(type(2 + 3)) # int type

e = 0XFF # hexadecimal
print(e) # 255
print(type(e)) # <class 'int'>

f = 0b11111111 # binary
print(f) # 255
print(type(f)) # <class 'int'>

g = 0o377 # octal
print(g) # 255
print(type(g)) # <class 'int'>


# floating points
print(2.0 + 5) # 7.0
print(23 / 5) # 4.6
print(23 // 5) # 4
print(25 % 4) # 1

