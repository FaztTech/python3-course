print( 3 > 2 ) # True
print( 3 >= 2 ) # True
print( 3 < 2 ) # False
print( 3 <= 2 ) # False
print( 3 == 2 ) # False
print( 3 != 2 )
# print( 3 <> 2 ) # Python 2 only
print( 3 in [1, 2, 3] )
print( 3 not in [1, 2, 3] )
print( 3 in (1, 2, 3) )
print( 3 not in (1, 2, 3) )
print( 3 in {1, 2, 3} )
print( 3 not in {1, 2, 3} )
print( 3 in {1: 'a', 2: 'b', 3: 'c'} )
print( 3 not in {1: 'a', 2: 'b', 3: 'c'} )

# with logical operators
print( 3 > 2 and 2 < 1 ) # False
print( 3 > 2 or 2 < 1 ) # True
print( not 3 > 2 ) # False
print( not 3 < 2 ) # True
print( not False ) # True
print( not True ) # False

