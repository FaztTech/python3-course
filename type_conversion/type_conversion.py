# int to float
balance = 30
print(balance)
print(type(balance))

balance_float = float(balance)
print(balance_float)
print(type(balance_float))

# float to int
height = 1.73
print(height)
print(type(height))

height_int = int(height)
print(height_int)
print(type(height_int))

# int to str
age = 30
print(age)
print(type(age))

age_str = str(age)
print(age_str)
print(type(age_str))

# str to int
age_str = "30"
print(age_str)
print(type(age_str))

age_int = int(age_str)
print(age_int)
print(type(age_int))

# str to float
age_str = "30"
print(age_str)
print(type(age_str))

age_float = float(age_str)
print(age_float)
print(type(age_float))

# convert to bool
print(bool(0))
print(bool(1))
print(bool(2))
print(bool(-1))
print(bool(0.0))

# convert to list
print(list("hello"))
print(list((1, 2, 3)))
print(list({1, 2, 3}))

# convert to tuple
print(tuple("hello"))
print(tuple([1, 2, 3]))

# convert to set
print(set("hello"))
print(set([1, 2, 3]))
print(set((1, 2, 3)))

# convert to dict
print(dict(a=1, b=2))
print(dict(zip(["a", "b"], [1, 2])))
print(dict([("a", 1), ("b", 2)]))

# convert to str
print(str(1))
print(str(1.0))

# complex
print(complex(1, 2))
print(complex(1))
